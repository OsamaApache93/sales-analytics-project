--5-Magnitude Analysis
--Find total customers by Country 
Select Country , COUNT(customer_key) as Total_customers from gold.dim_customers group by country

--Find total customers by gender 
Select gender , COUNT(customer_key) as Total_customers from gold.dim_customers group by gender

--Find total Products by Category
Select category , COUNT(distinct product_key) as Total_Products from gold.dim_products group by category order by Total_Products desc

--What are the average costs in each category?
Select category , avg(cost) as AVR_cost from gold.dim_products group by category 

--What is the Total revenue generated for each category? 
select p.category, sum ( f.sales_amount) as total_revenue
from gold.fact_sales f 
left join gold.dim_products p
on p.product_key = f. product_key
group by p.category  order  by total_revenue asc 

--Find total revenue generated by each customer
select 
c.first_name,c.last_name, sum ( f.sales_amount) as total_revenue
from gold.fact_sales f 
left join gold.dim_customers c
on c.customer_key = f. customer_key
group by c.first_name,c.last_name
order  by total_revenue desc 

-- What is the distribution of sold items across countries ? 
select c.country, sum ( f.quantity) as total_sold_amount
from gold.fact_sales f 
left join gold.dim_customers c
on c.customer_key = f. customer_key
group by country
order  by total_sold_amount desc 

--6-Ranking Analysis
-- Top 5 products by revenue
select top 5 
p.product_name , 
sum (f.sales_amount) total_revenue
from gold.fact_sales f
left join gold.dim_products p
on p.product_key = f.product_key
group by p.product_name
order by total_revenue Desc

--worst 5 performing products in terms of sales 
select top 5 
p.product_name , 
sum (f.sales_amount) total_revenue
from gold.fact_sales f
left join gold.dim_products p
on p.product_key = f.product_key
group by p.product_name
order by total_revenue 

-- Top 5 products by revenue using the Window Function 
select * from (
select 
    p.product_name , 
    sum (f.sales_amount) as total_revenue,
    ROW_NUMBER () OVER (order by sum(f.sales_amount) DESC ) as rank_products
   from gold.fact_sales f
    left join gold.dim_products p
on p.product_key = f.product_key
group by p.product_name ) as tbl
where rank_products <=5 

--Top 10 Customers generating revenue
