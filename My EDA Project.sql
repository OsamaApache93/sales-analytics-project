--Who are the top 10 customers generating the highest revenue& calculate average order value per customer
select  top 10
	c.customer_key ,c.first_name , c.last_name ,SUM(f.sales_amount) as Total_revenue , COUNT(distinct f.order_number ) as Orders , SUM(f.sales_amount) /COUNT(distinct f.order_number ) as AVG_Order_value
	from gold.fact_sales f 
